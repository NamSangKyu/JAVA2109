
public class VariableMain {
	/*
	 * 변수(Variable)
	 * 		: 단어 뜻 그대로 풀이 ---> 변하는 수
	 * 
	 * 데이터를 저장하는 방법을 배우는 중
	 * 데이터를 저장할려면 메인메모리에 저장
	 * 
	 * 변수타입 : 저장할 데이터 종류(숫자, 문자,.....)
	 * 
	 * 변수 선언 방법
	 * 		변수타입 변수명;
	 * 
	 * 변수는 값을 1개만 저장
	 */
	public static void main(String[] args) {
		int num;//변수를 선언 ---> 값을 저장할 메모리 공간을 할당받았다.
		num=10; //변수 num에 데이터 10을 저장
		num=20; //새로운 값이 들어오면 이전값은 사라짐
		System.out.println(num);
	}

}

