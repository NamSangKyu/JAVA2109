
public class CharType {

	public static void main(String[] args) {
		char ch1 = 'A';
		System.out.println(ch1);
		int n1 = ch1; //문자코드 값을 숫자로 형변환
		System.out.println(n1);
		ch1++;
		n1 = ch1;
		System.out.println(ch1);
		System.out.println(n1);
		
		char ch2 = 'Z';
		int n2 = ch2;
		System.out.println(ch2);
		System.out.println(n2);
		
	}

}
