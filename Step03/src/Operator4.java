
public class Operator4 {
	/*
	 * 		관계연산자(비교연산자)
	 * 		>    <    >=   <=   ==    !=
	 * 
	 */
	public static void main(String[] args) {
		int n1 = 10;
		int n2 = 7;
		
		System.out.println(n1 > n2);
		System.out.println(n1 >= n2);
		System.out.println(n1 < n2);
		System.out.println(n1 <= n2);
		System.out.println(n1 == n2);
		System.out.println(n1 != n2);
		System.out.println("---------------------");
		boolean flag = true;
		System.out.println(flag);
		flag = false;
		System.out.println(flag);
		flag = n1 > n2;
		System.out.println(flag);

	}

}






